class BluetoothConnection(val context: Context, val intent: Intent) {

    companion object {
        var m_myUUID: UUID = UUID.fromString("00001101-0000-1000-8000-00805F9B34FB")
        var m_bluetoothSocket: BluetoothSocket? = null
        lateinit var m_progress: ProgressDialog
        lateinit var m_bluetoothAdapter: BluetoothAdapter
        var m_isConnected: Boolean = false
        lateinit var m_address: String
    }

    fun getBluetoothSocket(): BluetoothSocket? {
        return m_bluetoothSocket
    }

    fun setConnection() {
        m_address = intent.getStringExtra(BluetoothActivity.EXTRA_ADDRESS)
        ConnectToDevice(context).execute()
    }

    /*
